package ch02;

public class Ex02_정수리터럴 {
	public static void main(String[] args) {
		// 2진수: 0b___
		int var1 = 0b1011;
		// 8진수: 0___
		int var2 = 0206;
		// 10진수
		int var3 = 365;
		// 16진수: 0x__
		int var4 = 0xB3;
		System.out.println("var1: " + var1);
		System.out.println("var2: " + var2);
		System.out.println("var3: " + var3);
		System.out.println("var4: " + var4);
	}
}
